    private void insertaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertaActionPerformed
        String name1 = namea.getText();
        String PATTERN ="^[a-zA-Z]{1,60}$";
        Pattern pt = Pattern.compile(PATTERN);
        Matcher match=pt.matcher(namea.getText());
        if(!match.matches()){
            JOptionPane.showMessageDialog(this, "Please enter your name only contains character!");
        }
        String PATTERN1 ="((?=.*\\d)(?=.*[a-z])(?=.*[A-Z]).{8,50})";
        Pattern pt1 =Pattern.compile(PATTERN1);
        Matcher match1=pt1.matcher(passworda.getText());
        if(!match1.matches()){
            JOptionPane.showMessageDialog(this, "Password must contain at least one digit, one lowercase,one uppercase character, length from 8 to 50 characters!");
        }
        String address1 = addressa.getText();
        if(address1.equals("")){
           JOptionPane.showMessageDialog(this, "Please enter your address!");
        }
        String PATTERN2 ="^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
        Pattern pt2 =Pattern.compile(PATTERN2);
        Matcher match2 = pt2.matcher(emaila.getText());
        if(!match2.matches()){
            JOptionPane.showMessageDialog(this, "Email invalid!\nExample: parkjyeon@gmail.com");
        }
        String birthday1= ((JTextField)birthdaya.getDateEditor().getUiComponent()).getText();
        if(birthday1.equals("")){
           JOptionPane.showMessageDialog(this, "Please enter your birthday!");
        }
        if(match.matches()&&match1.matches()&&!address1.equals("")&&match2.matches()&&!birthday1.equals("")){
        
         try{
             Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
           conn = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;"+"databaseName=KBCQuizDatabase","sa","sunflower");
              String check = "select * from Admin where Adminname=?";
              PreparedStatement pst1 = conn.prepareStatement(check);
              pst1.setString(1,namea.getText());
              ResultSet rs = pst1.executeQuery();
              if(rs.next()){
                  JOptionPane.showMessageDialog(this, "Name has already used!");
              }
              
              else{
              PreparedStatement pst = conn.prepareStatement("insert into Admin(Adminname,Password,Gender,Address,Email,Birthday,Status) values(?,?,?,?,?,?,?)");
              pst.setString(1,namea.getText());
              pst.setString(2,passworda.getText());
              pst.setInt(3, gender); 
              pst.setString(4,addressa.getText());  
              pst.setString(5,(emaila.getText()));         
                      
              pst.setString(6,((JTextField)birthdaya.getDateEditor().getUiComponent()).getText());
             
              pst.setString(7, statusa.getText());
              int result = pst.executeUpdate();
              if(result>0){
                  JOptionPane.showMessageDialog(this, "Insert successfully!");
                  tb.setRowCount(0);
                  load();
                  clear();
              }
              else{
                  JOptionPane.showMessageDialog(this, "Insert unsucessufuly!");
              }
         }
              clear();
         }
         catch(Exception e){
             System.out.println(e.getMessage());
         }
        }
    }//GEN-LAST:event_insertaActionPerformed

