    public void load(){
        try{
           Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
           conn = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;"+"databaseName=KBCQuizDatabase","sa","sunflower");
           int number;
           Vector row, column;
           column = new Vector();
           Statement st = conn.createStatement();
           ResultSet rs= st.executeQuery("select * from Admin");
           ResultSetMetaData metadata= rs.getMetaData();
           number = metadata.getColumnCount();
           for(int i = 1;i<=number;i++){
               column.add(metadata.getColumnName(i));
           }
           tb.setColumnIdentifiers(column);
           while(rs.next()){
               row = new Vector();
               for(int i=1;i<=number;i++){
                  row.addElement(rs.getString(i));
               }
               tb.addRow(row);
               dataadmin.setModel(tb);
           }
           dataadmin.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
               @Override
               public void valueChanged(ListSelectionEvent e) {
                   if(dataadmin.getSelectedRow()>=0){                    
                      namea.setText(dataadmin.getValueAt(dataadmin.getSelectedRow(), 0)+ "");
                      passworda.setText(dataadmin.getValueAt(dataadmin.getSelectedRow(), 1)+ "");
                       addressa.setText(dataadmin.getValueAt(dataadmin.getSelectedRow(), 3)+ "");
                     emaila.setText(dataadmin.getValueAt(dataadmin.getSelectedRow(), 4)+ "");  
                    ((JTextField)birthdaya.getDateEditor().getUiComponent()).setText(dataadmin.getValueAt(dataadmin.getSelectedRow(), 5)+ "");
                     statusa.setText(dataadmin.getValueAt(dataadmin.getSelectedRow(), 6)+ "");

                      
                   }
               }
           });
            
                
             
                
                
       }
       catch(Exception e){
           System.out.println(e.getMessage());
       }
    }
                
    

